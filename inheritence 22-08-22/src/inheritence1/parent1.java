package inheritence1;

public class parent1 {
	
	int num1;
	int num2;
	int num3;
	String name;
	
	public parent1()
	{
		System.out.println("parent constructer");
	}
	
	public parent1(int num1, int num2, int num3, String name)
	{
		this.num1=num1;
		this.num2=num2;
		this.num3=num3;
		this.name=name;
		
	}
	

	

}
